#include <bits/stdc++.h>
using namespace std;

int t,a,b;

int main () {
    cin >> t;

    while(t--){
        cin>>a>>b;
        if(a==b){
            cout<<0<<"\n";
        }
        else{
            if(abs(a-b)%10!=0)
            cout<<abs(a-b)/10+1<<"\n";
        else
            cout<<abs(a-b)/10<<"\n";
        }
    }
}
